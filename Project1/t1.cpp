void openfile(string file){
	ifstream infile;
	infile.open(file.data());
	assert(infile.is_open());

	string s;
	int countline = 0;
	int totalpoint = 0;
	int totalsrf = 0;
	char *p;
	const char* split = " ";
	while(getline(infile, s)){
		if(countline == 0)
		{
			totalpoint = atoi(s.c_str());
			points = (GLfloat*)malloc(sizeof(GLfloat) * totalpoint * 3);
			countline ++;
			totalpoint += 2;
		}
		else if(countline == 1)
		{
			totalsrf = atoi(s.c_str());
			indices = (GLuint*)malloc(sizeof(GLuint) * totalsrf *3);
			countline++;
		}
		else
		{
			if(countline < totalpoint)
			{
				p = strtok(const_cast<char*>(s.c_str()), split);
				points[point_index ++] = atof(p);
				p = strtok(NULL, split);
				points[point_index ++] = atof(p);
				p = strtok(NULL, split);
				points[point_index ++] = atof(p);
			}
			else
			{
				p = strtok(const_cast<char*>(s.c_str()), split);
				indices[indice_index ++] = atof(p);
				p = strtok(NULL, split);
				indices[indice_index ++] = atof(p);
				p = strtok(NULL, split);
				indices[indice_index ++] = atof(p);
			}
			countline++;
		}
	}
	infile.close();
}


